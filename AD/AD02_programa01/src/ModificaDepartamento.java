    public void rellenarDatos(JAXBElement jaxbElement) {
        
        this.jaxbElement = jaxbElement;
        // El m√©todo getValue() retorna el modelo de contenido (content model) y el valor de los atributos del elemento
        DatosempledepartType datosempledeparttype = (DatosempledepartType) jaxbElement.getValue();
        // Obtenemos una instancia de tipo PedidoType para obtener un Objeto de tipo Direccion    
        departamento = datosempledeparttype.getDepartamento();
        
        codigo.setText(departamento.getCodigodep().toString());
        nombre.setText(departamento.getNombredep());
        localidad.setText(departamento.getLocalidad());
        presupuesto.setText(departamento.getPresupuesto().toString());

    }


